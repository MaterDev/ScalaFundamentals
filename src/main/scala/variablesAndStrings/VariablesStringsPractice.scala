package variablesAndStrings

@main
def variablesStringsPractice(): Unit = {
  val myCatSentence = "My cat's name is \"Fluffy\""
  println(myCatSentence)

  val slashes = "Two types of slashes \\ /"
  println(slashes)

  val url = "http://www.google.com"
  println(url)

  val bunnyLine_1 = "(\\(\\"
  val bunnyLine_2 = "(=.-)"
  val bunnyLine_3 = "o_(\")(\")"

  println(bunnyLine_1)
  println(bunnyLine_2)
  println(bunnyLine_3)
}